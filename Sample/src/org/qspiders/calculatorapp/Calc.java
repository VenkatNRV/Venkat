package org.qspiders.calculatorapp;

public class Calc {

	// adding two numbers
	int add(int num1, int num2) {
		int res = num1 + num2;
		return res;
	}

	// adding three numbers
	int add(int num1, int num2, int num3) {
		return num1 + num2 + num3;
	}

	// adding four numbers
	int add(int nu1, int nu2, int nu3, int nu4) {
		int res = nu1 + nu2 + nu3 + nu4;
		return res;
	}
}
